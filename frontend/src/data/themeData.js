/**
 * Получить цвет для научной области
 * @param {string} field - название научной области
 * @returns {string} цвет в HEX
 */
export const getFieldColor = (field) => {
  if (!field) return '#f0f4f8'; // мягкий серо-голубой для пустых полей

  // Сначала ищем точное совпадение
  if (FIELD_PASTEL_COLORS[field]) {
    return FIELD_PASTEL_COLORS[field];
  }

  // Ищем частичное совпадение
  for (const [key, color] of Object.entries(FIELD_PASTEL_COLORS)) {
    if (field.includes(key) || key.includes(field)) {
      return color;
    }
  }

  // Если ничего не нашли, возвращаем цвет на основе первой буквы
  const firstLetter = field.charAt(0).toLowerCase();
  const defaultColors = {
    'а': '#f8d7da', 'б': '#d4edda', 'в': '#cce5ff', 'г': '#fff3cd',
    'д': '#d1ecf1', 'е': '#fbe9e7', 'ж': '#e8eaf6', 'з': '#f3e5f5',
    'и': '#e0f2f1', 'к': '#fff8e1', 'л': '#f1f8e9', 'м': '#ede7f6',
    'н': '#e1f5fe', 'о': '#fff3e0', 'п': '#e8f5e9', 'р': '#ffebee',
    'с': '#e0f7fa', 'т': '#f9fbe7', 'у': '#efebe9', 'ф': '#fce4ec',
    'х': '#e1bee7', 'ц': '#b2ebf2', 'ч': '#b2dfdb', 'ш': '#b39ddb',
    'э': '#bcaaa4', 'ю': '#b0bec5', 'я': '#c8e6c9',
  };

  return defaultColors[firstLetter] || '#e9ecef';
};

/**
 * Получить иконку для компетенции
 * @param {string} competency - название компетенции
 * @returns {string} иконка-эмодзи
 */
export const getCompetencyIcon = (competency) => {
  if (!competency) return '•';

  // Точное совпадение
  if (COMPETENCY_ICONS[competency]) {
    return COMPETENCY_ICONS[competency];
  }

  // Частичное совпадение
  for (const [key, icon] of Object.entries(COMPETENCY_ICONS)) {
    if (competency.includes(key)) {
      return icon;
    }
  }

  return '•';
};

