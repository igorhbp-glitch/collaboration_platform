/**
 * Возвращает уникальные названия программ (без дубликатов)
 */
export const getUniqueProgramNames = () => {
  const names = STUDY_PROGRAMS.map(p => p.name);
  return [...new Set(names)].sort();
};

/**
 * Возвращает доступные уровни для конкретной программы
 */
export const getLevelsForProgram = (programName) => {
  const programs = STUDY_PROGRAMS.filter(p => p.name === programName);
  return programs.map(p => ({
    value: p.level,
    label: EDUCATION_LEVELS.find(l => l.value === p.level)?.label || p.level
  }));
};

/**
 * Возвращает плоский список всех научных направлений
 */
export const getAllResearchFields = () => {
  const allFields = [];
  Object.values(RESEARCH_FIELDS).forEach(category => {
    allFields.push(...category.items);
  });
  return allFields.sort();
};

/**
 * Поиск по всем справочникам
 */
export const searchScienceData = (query) => {
  if (!query || query.length < 2) return [];

  const results = [];
  const lowerQuery = query.toLowerCase();

  // Поиск по компетенциям (все категории)
  Object.values(COMPETENCIES).forEach(category => {
    category.items.forEach(item => {
      if (item.toLowerCase().includes(lowerQuery)) {
        results.push({
          type: 'Компетенция',
          category: category.name,
          value: item
        });
      }
    });
  });

  // Поиск по методологиям
  METHODOLOGIES.forEach(method => {
    if (method.toLowerCase().includes(lowerQuery)) {
      results.push({
        type: 'Методология',
        category: 'Методологии',
        value: method
      });
    }
  });

  // Поиск по специальностям (уникальные названия)
  getUniqueProgramNames().forEach(name => {
    if (name.toLowerCase().includes(lowerQuery)) {
      results.push({
        type: 'Специальность',
        category: 'Направления подготовки',
        value: name
      });
    }
  });

  // Поиск по филиалам
  UNIVERSITY_BRANCHES.forEach(branch => {
    if (branch.toLowerCase().includes(lowerQuery)) {
      results.push({
        type: 'Филиал',
        category: 'Филиалы',
        value: branch
      });
    }
  });

  // Поиск по должностям
  POSITIONS.forEach(pos => {
    if (pos.name.toLowerCase().includes(lowerQuery)) {
      results.push({
        type: 'Должность',
        category: pos.category === 'student' ? 'Обучающиеся' : 'Сотрудники',
        value: pos.name
      });
    }
  });

  // Поиск по научным направлениям
  Object.values(RESEARCH_FIELDS).forEach(category => {
    category.items.forEach(item => {
      if (item.toLowerCase().includes(lowerQuery)) {
        results.push({
          type: 'Научное направление',
          category: category.name,
          value: item
        });
      }
    });
  });

  return results.slice(0, 30);
};